# SECTION 3 — CONVERSATION TURN  ← NEW
# A single immutable record of one interview exchange.
# ================================================================

@dataclass(frozen=True)
class ConversationTurn:
    """
    Immutable record of one complete interview exchange.

    Fields
    ------
    turn_number  : 1-based index of this turn in the session
    question     : exact text received from the interviewer (post-preprocess)
    answer       : exact text generated by the LLM
    timestamp    : Unix time when the answer was generated
    tokens_sent  : estimated tokens in the context sent to the LLM
    tokens_recv  : estimated tokens in the response
    """
    turn_number:  int
    question:     str
    answer:       str
    timestamp:    float
    tokens_sent:  int = 0
    tokens_recv:  int = 0

    def to_dict(self) -> Dict[str, Any]:
        return {
            "turn":       self.turn_number,
            "question":   self.question,
            "answer":     self.answer,
            "timestamp":  self.timestamp,
            "tokens_sent": self.tokens_sent,
            "tokens_recv": self.tokens_recv,
        }

    def __str__(self) -> str:
        return (
            f"[Turn {self.turn_number}]\n"
            f"  Q: {self.question}\n"
            f"  A: {self.answer}\n"
        )


