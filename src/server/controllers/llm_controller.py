# SECTION 5 — LLMController  (with conversation log)
# ================================================================

class LLMController(BaseLLMConfigurationInterface):

    def __init__(self, params: Dict[str, Any] = None, system_message: str = None):
        if params is None:
            params = {
                "max_tokens":  get_config().MAX_TOKENS,
                "temperature": get_config().TEMPERATURE,
            }
        BaseLLMConfigurationInterface.__init__(self, params)
        self.config = get_config()
        self.SYSTEM_MESSAGE = SystemMessage(
            content=system_message.strip() if system_message else ""
        )

        # Legacy compatibility — kept empty, never sent to LLM
        self.history: List[BaseMessage] = []

        self._memory: Optional[HybridMemoryManager] = None
        self._budget: Optional[TokenBudget] = None

        # ── Conversation log ─────────────────────────────────────────
        # Plain list of ConversationTurn objects.
        # Grows by 1 per generate_response() call.
        # Never pruned — it is the audit trail, not the LLM context.
        # Thread-safe: protected by _log_lock.
        self._conversation_log: List[ConversationTurn] = []
        self._log_lock = threading.Lock()

    # ────────────────────────────────────────────
    # setup()
    # ────────────────────────────────────────────

    def setup(self) -> bool:
        if self.config.LLM_MODE == LLMExecutionMode.REMOTE.value:
            self.mode = LLMExecutionMode.REMOTE.value
            self._setup_remote_provider()
        elif self.config.LLM_MODE == LLMExecutionMode.LOCAL.value:
            self.mode = LLMExecutionMode.LOCAL.value
            self._setup_local_provider()
        else:
            self._is_connected = False
            raise ValueError(f"Unknown execution mode: {self.config.LLM_MODE}")

        if self.mode == LLMExecutionMode.LOCAL.value:
            ctx = getattr(self.config, "CONTEXT_WINDOW", 8192)
            self._budget = TokenBudget.for_local(context_window=ctx)
        else:
            ctx = getattr(self.config, "CONTEXT_WINDOW", 128000)
            self._budget = TokenBudget.for_remote(context_window=ctx)

        keep_n = 4 if self.mode == LLMExecutionMode.LOCAL.value else 8
        self._memory = HybridMemoryManager(
            llm=self.client,
            budget=self._budget,
            keep_last_n=keep_n,
        )
        return self._is_connected

    def _setup_remote_provider(self):
        provider = self.config.LLM_PROVIDER
        if provider == LLMProvider.GEMINI.value:
            self.api_key  = self.config.GEMINI_API_KEY
            self.model_id = self.config.GEMINI_MODEL_ID
            self.client   = ChatGoogleGenerativeAI(
                model=self.model_id, temperature=self.temperature,
                max_tokens=self.max_tokens, api_key=self.api_key,
            )
        elif provider == LLMProvider.OPENAI.value:
            self.api_key  = self.config.OPENAI_API_KEY
            self.model_id = self.config.OPENAI_MODEL_ID
            self.base_url = self.config.OPEN_AI_BASE_URL
            self.client   = ChatOpenAI(
                model=self.model_id, temperature=self.temperature,
                max_tokens=self.max_tokens, api_key=self.api_key,
                base_url=self.base_url,
            )
        elif provider == LLMProvider.CLAUDE.value:
            self.api_key  = self.config.CLAUDE_API_KEY
            self.model_id = self.config.CLAUDE_MODEL_ID
            self.client   = ChatAnthropic(
                model=self.model_id, temperature=self.temperature,
                max_tokens=self.max_tokens, api_key=self.api_key,
            )
        elif provider == LLMProvider.COHERE.value:
            self.api_key  = self.config.COHERE_API_KEY
            self.model_id = self.config.COHERE_MODEL_ID
            self.client   = ChatCohere(
                model=self.model_id, temperature=self.temperature,
                max_tokens=self.max_tokens, cohere_api_key=self.api_key,
            )
        else:
            self._is_connected = False
            raise ValueError(f"Unknown provider: {self.config.LLM_PROVIDER}")
        self._is_connected = True

    def _setup_local_provider(self):
        ctx = getattr(self.config, "CONTEXT_WINDOW", 8192)
        self.api_key  = getattr(self.config, "OLLAMA_API_KEY", None)
        self.model_id = self.config.OLLAMA_MODEL_ID
        self.base_url = self.config.OLLAMA_BASE_URL
        self.client   = ChatOllama(
            model=self.model_id, base_url=self.base_url,
            temperature=self.temperature, num_predict=self.max_tokens,
            num_ctx=ctx,
        )
        self._is_connected = True

    # ────────────────────────────────────────────
    # Public helpers
    # ────────────────────────────────────────────

    def update_system_message(self, text: str):
        self.SYSTEM_MESSAGE = SystemMessage(content=text.strip())

    def _estimate_tokens(self, text: str) -> int:
        return TokenEstimator.estimate(text)

    def _count_tokens(self, messages: List[BaseMessage]) -> int:
        return TokenEstimator.estimate_messages(messages)

    # ────────────────────────────────────────────
    # Memory helpers
    # ────────────────────────────────────────────

    def _build_messages_from_memory(self, current_human_msg: Optional[HumanMessage] = None) -> List[BaseMessage]:
        if self._memory is None:
            return [self.SYSTEM_MESSAGE]
        if current_human_msg is None:
            return [self.SYSTEM_MESSAGE] + list(self._memory.messages)
        return self._memory.build_context(self.SYSTEM_MESSAGE, current_human_msg)

    def _save_turn_to_memory(self, user_input: str, ai_response: str):
        if self._memory is not None:
            self._memory.add_messages([
                HumanMessage(content=user_input.strip()),
                AIMessage(content=ai_response.strip()),
            ])

    def release_history(self):
        self.history = []
        if self._memory is not None:
            self._memory.clear()
        # NOTE: _conversation_log is NOT cleared here intentionally.
        # release_history() resets LLM context but preserves the audit log.
        # Call reset_log() explicitly if you also want the log cleared.

    # ────────────────────────────────────────────
    # Conversation log helpers  ← NEW
    # ────────────────────────────────────────────

    def _record_turn(self, question: str, answer: str, tokens_sent: int = 0) -> None:
        """Append a completed turn to the conversation log. Thread-safe."""
        with self._log_lock:
            turn = ConversationTurn(
                turn_number  = len(self._conversation_log) + 1,
                question     = question,
                answer       = answer,
                timestamp    = time.time(),
                tokens_sent  = tokens_sent,
                tokens_recv  = TokenEstimator.estimate(answer),
            )
            self._conversation_log.append(turn)

    def get_report(self) -> List[ConversationTurn]:
        """
        Returns the full ordered list of ConversationTurn objects
        for the current session.

        Usage
        -----
        turns = controller.get_report()
        for t in turns:
            print(t)                 # pretty print
            d = t.to_dict()          # dict for JSON / DB
        """
        with self._log_lock:
            return list(self._conversation_log)   # shallow copy, safe to iterate

    def get_report_as_dicts(self) -> List[Dict[str, Any]]:
        """
        Convenience method — returns list of plain dicts.
        Ready for json.dumps(), pandas.DataFrame(), or DB insert.

        Usage
        -----
        import json
        print(json.dumps(controller.get_report_as_dicts(), indent=2))
        """
        return [t.to_dict() for t in self.get_report()]

    def get_report_as_text(self) -> str:
        """
        Human-readable transcript of the full session.

        Usage
        -----
        print(controller.get_report_as_text())
        # or save to file:
        # with open("report.txt", "w") as f:
        #     f.write(controller.get_report_as_text())
        """
        turns = self.get_report()
        if not turns:
            return "No conversation recorded yet."

        lines = ["=" * 60, "INTERVIEW SESSION TRANSCRIPT", "=" * 60, ""]
        for t in turns:
            lines.append(f"[Turn {t.turn_number}]")
            lines.append(f"  Interviewer : {t.question}")
            lines.append(f"  Interviewee : {t.answer}")
            lines.append(f"  Tokens sent : {t.tokens_sent}  |  received : {t.tokens_recv}")
            lines.append("")
        lines.append("=" * 60)
        lines.append(f"Total turns  : {len(turns)}")
        lines.append(f"Total tokens sent    : {sum(t.tokens_sent for t in turns)}")
        lines.append(f"Total tokens received: {sum(t.tokens_recv for t in turns)}")
        lines.append("=" * 60)
        return "\n".join(lines)

    def reset_log(self) -> None:
        """
        Wipe the conversation log.
        Call this when starting a brand-new interview session
        (alongside release_history() to also reset LLM context).
        """
        with self._log_lock:
            self._conversation_log = []

    # ────────────────────────────────────────────
    # generate_response()
    # ────────────────────────────────────────────

    def generate_response(self, user_input: str) -> str:
        if not self._is_connected:
            raise ConnectionError("LLM Client is not connected. Call setup() first.")
        if not self.SYSTEM_MESSAGE or not self.SYSTEM_MESSAGE.content.strip():
            raise ValueError("System message is empty. Call update_system_message() first.")
        if not user_input.strip():
            raise ValueError("User input is empty.")

        human_message = HumanMessage(content=user_input.strip())

        if self._memory is not None:
            messages_to_send = self._memory.build_context(self.SYSTEM_MESSAGE, human_message)
            logger.debug("Memory stats: %s", self._memory.get_stats())
        else:
            messages_to_send = [self.SYSTEM_MESSAGE, human_message]

        tokens_sent = TokenEstimator.estimate_messages(messages_to_send)

        response = self._invoke_with_fallback(messages_to_send, human_message)

        if self._memory is not None:
            self._save_turn_to_memory(user_input, response.content)

        # ── Record to conversation log ────────────────────────────────
        self._record_turn(
            question    = user_input,
            answer      = response.content,
            tokens_sent = tokens_sent,
        )

        return response.content

    def _invoke_with_fallback(self, messages: List[BaseMessage], human_message: HumanMessage):
        try:
            return self.client.invoke(messages)
        except Exception as e:
            err = str(e).lower()
            if not ("context" in err or "exceed" in err or "token" in err
                    or "length" in err or "too long" in err):
                raise
            logger.warning("Context window hit — Level 1 fallback: %s", e)
            if self._memory is not None:
                self._memory.soft_clear()
                messages = self._memory.build_context(self.SYSTEM_MESSAGE, human_message)
            else:
                messages = [self.SYSTEM_MESSAGE, human_message]
            try:
                return self.client.invoke(messages)
            except Exception as e2:
                logger.warning("Level 1 failed — Level 2 fallback: %s", e2)
                if self._memory is not None:
                    self._memory.clear()
                return self.client.invoke([self.SYSTEM_MESSAGE, human_message])

    # ────────────────────────────────────────────
    # Async support
    # ────────────────────────────────────────────

    async def agenerate_response(self, user_input: str) -> str:
        if not self._is_connected:
            raise ConnectionError("LLM Client is not connected.")
        if not user_input.strip():
            raise ValueError("User input is empty.")

        human_message = HumanMessage(content=user_input.strip())

        if self._memory is not None:
            messages_to_send = self._memory.build_context(self.SYSTEM_MESSAGE, human_message)
        else:
            messages_to_send = [self.SYSTEM_MESSAGE, human_message]

        tokens_sent = TokenEstimator.estimate_messages(messages_to_send)
        response    = await self._ainvoke_with_fallback(messages_to_send, human_message)

        if self._memory is not None:
            self._save_turn_to_memory(user_input, response.content)

        self._record_turn(user_input, response.content, tokens_sent)
        return response.content

    async def _ainvoke_with_fallback(self, messages, human_message):
        try:
            return await self.client.ainvoke(messages)
        except Exception as e:
            err = str(e).lower()
            if not ("context" in err or "exceed" in err or "token" in err):
                raise
            if self._memory is not None:
                self._memory.soft_clear()
                messages = self._memory.build_context(self.SYSTEM_MESSAGE, human_message)
            return await self.client.ainvoke(messages)

    # ────────────────────────────────────────────
    # Streaming support
    # ────────────────────────────────────────────

    def stream_response(self, user_input: str):
        if not self._is_connected:
            raise ConnectionError("LLM not connected.")
        if not user_input.strip():
            raise ValueError("Empty input.")

        human_message = HumanMessage(content=user_input.strip())

        if self._memory is not None:
            messages_to_send = self._memory.build_context(self.SYSTEM_MESSAGE, human_message)
        else:
            messages_to_send = [self.SYSTEM_MESSAGE, human_message]

        tokens_sent   = TokenEstimator.estimate_messages(messages_to_send)
        full_response = []

        for chunk in self.client.stream(messages_to_send):
            text = chunk.content
            full_response.append(text)
            yield text

        if full_response:
            complete = "".join(full_response)
            if self._memory is not None:
                self._save_turn_to_memory(user_input, complete)
            self._record_turn(user_input, complete, tokens_sent)

    # ────────────────────────────────────────────
    # Cleanup
    # ────────────────────────────────────────────

    def cleanup(self):
        self.client         = None
        self.history        = []
        self._is_connected  = False
        self.SYSTEM_MESSAGE = None
        if self._memory is not None:
            self._memory.clear()
        self._memory = None
        # _conversation_log is intentionally kept alive after cleanup
        # so get_report() still works after the session ends.