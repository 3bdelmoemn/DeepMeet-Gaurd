# SECTION 4 — HYBRID MEMORY MANAGER  (unchanged)
# ================================================================

class HybridMemoryManager(BaseChatMessageHistory, BaseModel):
    messages:              List[BaseMessage] = Field(default_factory=list)
    moving_summary_buffer: str               = Field(default="")
    key_facts:             List[str]         = Field(default_factory=list)
    keep_last_n:           int               = Field(default=6)
    _dirty:                bool              = False

    llm:    Any = Field(default=None)
    budget: Any = Field(default=None)

    class Config:
        arbitrary_types_allowed = True

    _lock: threading.RLock = None  # type: ignore

    def model_post_init(self, __context: Any) -> None:
        object.__setattr__(self, '_lock', threading.RLock())
        object.__setattr__(self, '_dirty', False)

    def _msg_hash(self, msg: BaseMessage) -> str:
        return hashlib.md5(
            f"{type(msg).__name__}:{msg.content}".encode()
        ).hexdigest()[:12]

    def _token(self, text: str) -> int:
        return TokenEstimator.estimate(text)

    def _token_msgs(self, msgs: List[BaseMessage]) -> int:
        return TokenEstimator.estimate_messages(msgs)

    _FACT_PATTERNS = re.compile(
        r"(I (?:use|am using|work with|prefer|have)\b.{10,80}|"
        r"\d+\s*(?:k|K|ms|MB|GB|RPS|req|nodes?|cores?|workers?)\b.{0,40}|"
        r"(?:must|should|need to|required to)\s.{10,60}|"
        r"(?:the system|architecture|stack|language|framework)\s.{10,60})",
        re.IGNORECASE,
    )

    def _extract_facts(self, text: str) -> List[str]:
        found = []
        for m in self._FACT_PATTERNS.finditer(text):
            fact = m.group().strip().rstrip(".,;")
            if 10 < len(fact) < 120 and fact not in self.key_facts:
                found.append(fact)
        return found[:3]

    def add_messages(self, messages: List[BaseMessage]) -> None:
        with self._lock:
            existing_hashes = {self._msg_hash(m) for m in self.messages}
            added = False
            for msg in messages:
                if self._msg_hash(msg) in existing_hashes:
                    continue
                self.messages.append(msg)
                existing_hashes.add(self._msg_hash(msg))
                added = True
                if isinstance(msg, HumanMessage):
                    self.key_facts.extend(self._extract_facts(msg.content))
            if added:
                object.__setattr__(self, '_dirty', True)

    def clear(self) -> None:
        with self._lock:
            self.messages = []
            self.moving_summary_buffer = ""
            self.key_facts = []
            object.__setattr__(self, '_dirty', False)

    def soft_clear(self) -> None:
        with self._lock:
            keep = max(2, len(self.messages) // 2)
            self.messages = self.messages[-keep:]
            object.__setattr__(self, '_dirty', True)

    def _should_evict(self) -> bool:
        if self.budget is None:
            return len(self.messages) > self.keep_last_n * 2
        return self._token_msgs(self.messages) > self.budget.hot_budget

    def _evict_to_summary(self) -> None:
        if not self.llm:
            self.messages = self.messages[-(self.keep_last_n * 2):]
            return
        keep_count = self.keep_last_n * 2
        if len(self.messages) <= keep_count:
            return
        to_evict      = self.messages[:-keep_count]
        self.messages = self.messages[-keep_count:]
        new_summary   = self._call_summarizer(self.moving_summary_buffer, to_evict)
        if self.budget and self._token(new_summary) > self.budget.summary_max:
            words = new_summary.split()
            target_words = int(self.budget.summary_max * 3.5)
            new_summary = " ".join(words[:target_words]) + " [truncated]"
        self.moving_summary_buffer = new_summary
        object.__setattr__(self, '_dirty', False)

    def _call_summarizer(self, existing_summary: str, messages_to_summarize: List[BaseMessage]) -> str:
        lines = "\n".join(
            f"{'Human' if isinstance(m, HumanMessage) else 'AI'}: {m.content[:600]}"
            for m in messages_to_summarize
        )
        prompt = (
            "You are summarizing a technical interview conversation. "
            "Preserve ALL technical details: algorithms, data structures, "
            "system design decisions, code patterns, complexity analysis, "
            "and any constraints mentioned. Be concise but technically precise.\n\n"
            f"EXISTING SUMMARY:\n{existing_summary or '(none)'}\n\n"
            f"NEW CONVERSATION TURNS:\n{lines}\n\n"
            "UPDATED SUMMARY (max 200 words, dense technical facts only):"
        )
        try:
            resp = self.llm.invoke([HumanMessage(content=prompt)])
            return resp.content.strip()
        except Exception as e:
            logger.warning("Summarization failed, keeping existing: %s", e)
            return existing_summary

    def build_context(self, system_message: SystemMessage, current_human_msg: HumanMessage) -> List[BaseMessage]:
        with self._lock:
            if self._should_evict() and self._dirty:
                self._evict_to_summary()
            result: List[BaseMessage] = [system_message]
            if self.key_facts:
                unique_facts = list(dict.fromkeys(self.key_facts))[-12:]
                result.append(SystemMessage(
                    content="Key technical context:\n• " + "\n• ".join(unique_facts)
                ))
            if self.moving_summary_buffer.strip():
                result.append(SystemMessage(
                    content=f"[Earlier conversation summary]:\n{self.moving_summary_buffer.strip()}"
                ))
            result.extend(self.messages)
            result.append(current_human_msg)
            return result

    def get_stats(self) -> Dict[str, Any]:
        with self._lock:
            return {
                "hot_messages":   len(self.messages),
                "hot_tokens":     self._token_msgs(self.messages),
                "summary_tokens": self._token(self.moving_summary_buffer),
                "key_facts_count": len(self.key_facts),
                "is_dirty":       self._dirty,
                "budget_hot_max": self.budget.hot_budget if self.budget else None,
            }


