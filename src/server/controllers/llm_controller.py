# SECTION 1 — TOKEN BUDGET
# ================================================================

@dataclass
class TokenBudget:
    total_context:    int   = 8192
    system_reserve:   int   = 512
    response_reserve: int   = 1024
    summary_max:      int   = 600
    user_turn_max:    int   = 800
    hot_buffer_pct:   float = 0.45

    @property
    def available(self) -> int:
        return (self.total_context
                - self.system_reserve
                - self.response_reserve
                - self.summary_max
                - self.user_turn_max)

    @property
    def hot_budget(self) -> int:
        return max(400, int(self.available * self.hot_buffer_pct))

    @classmethod
    def for_local(cls, context_window: int = 8192) -> "TokenBudget":
        return cls(
            total_context=context_window,
            system_reserve=400,
            response_reserve=1024,
            summary_max=400,
            user_turn_max=600,
            hot_buffer_pct=0.40,
        )

    @classmethod
    def for_remote(cls, context_window: int = 128000) -> "TokenBudget":
        return cls(
            total_context=min(context_window, 32000),
            system_reserve=800,
            response_reserve=2048,
            summary_max=1200,
            user_turn_max=2000,
            hot_buffer_pct=0.55,
        )


# ================================================================
# SECTION 2 — TOKEN ESTIMATOR
# ================================================================

class TokenEstimator:
    _CODE_BLOCK_RE = re.compile(r"```[\s\S]*?```")

    @classmethod
    def estimate(cls, text: str) -> int:
        if not text:
            return 0
        code_chars  = sum(len(m.group()) for m in cls._CODE_BLOCK_RE.finditer(text))
        prose_chars = len(text) - code_chars
        return int(prose_chars / 4.0) + int(code_chars / 3.2)

    @classmethod
    def estimate_messages(cls, msgs: List[BaseMessage]) -> int:
        return sum(cls.estimate(m.content) for m in msgs)


